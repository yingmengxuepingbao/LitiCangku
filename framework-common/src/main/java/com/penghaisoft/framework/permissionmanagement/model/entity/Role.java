package com.penghaisoft.framework.permissionmanagement.model.entity;

import lombok.Data;

import java.util.Date;
import java.util.List;

/**
 * 角色实体类
 */
public class Role {
    private Integer id; //id

    private String name;//角色名称

    private String description;//角色描述

    private Date createdTime;//角色创建时间

    private List<Resources> resourceList; //关联的资源列表

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Date getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Date createdTime) {
        this.createdTime = createdTime;
    }

    public List<Resources> getResourceList() {
        return resourceList;
    }

    public void setResourceList(List<Resources> resourceList) {
        this.resourceList = resourceList;
    }
}